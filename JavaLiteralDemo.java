public class JavaLiteralDemo {
    public static void main(String[] args) {

        int decimal = 25;
        int binary = 0b11001;
        float floatNum = 5.5f;
        char letter = 'A';

        System.out.println("Decimal: " + decimal);
        System.out.println("Binary: " + binary);
        System.out.println("Float: " + floatNum);
        System.out.println("Char: " + letter);
    }
}